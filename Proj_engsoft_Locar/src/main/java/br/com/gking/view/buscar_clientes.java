    private void buscarBotaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarBotaoActionPerformed
        try {
            var cliente = database.consultarCliente(Long.parseLong(buscaText.getText()));
            
            if(cliente != null){
                var gerenciarScreen = new gerenciar_clientes(cliente);
            
                gerenciarScreen.modoConsulta();
                gerenciarScreen.show();
                dispose();
            } else {
                String[] options = { "Cadastrar", "Cancelar"};
                JOptionPane.showMessageDialog(rootPane, "Cliente n√£o cadastrado!");
                var selection = JOptionPane.showOptionDialog(rootPane, 
                                        "Deseja cadastrar um novo cliente?", 
                                        "Cadastrar cliente", 
                                        0, 3, 
                                        null, options, options[1]);
                if(selection == 0){
                    cliente = new Cliente(Long.parseLong(buscaText.getText()), "", 0L, "");
                    var gerenciarScreen = new gerenciar_clientes(cliente);
                    
                    gerenciarScreen.modoCadastro();
                    gerenciarScreen.show();
                    dispose();
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(buscar_clientes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_buscarBotaoActionPerformed

